
/*
Operadores de comparacao 
> maior
>= maior ou igual
< menor
<= menor ou igual
== igualdade (valor)
=== igualdade estrita (valor e tipo)
!= diferente (valor)
!== diferente estrito ( vlaor e tipo )
*/

//       >
const comp = 10 > 5;
console.log(comp);


//     >= 
const comp1 = 10 >= 45;
console.log(comp1);

//     < 
const comp2 = 10 < 14;
console.log(comp2);

//     <= 
const comp3 = 20 <= 14;
console.log(comp3);

//     ==  // nao utilizamos esse
const comp4 = 10 == 10;
console.log(comp4);


//     ===
const comp5 = 10 === 11;
console.log(comp5);


//     !=  // nao utilizamos esse tambem
const comp6 = 10 != 12;
console.log(comp6);


//     !=
const comp7 = 12 !== 12;
console.log(comp7);